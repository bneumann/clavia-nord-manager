using NordSampleManager.Protocol.Framing;

namespace NordSampleManager.Protocol.Tests;

public class Crc16Ibm3740Tests
{
    [Fact]
    public void Matches_InitFrame_FromCapture()
    {
        // Message 0 from parsed_protocol.txt: CMD_INIT host->device.
        // length=0x12, cmd=0x07, param1=0x00, param2=0x02, no payload, CRC=0x6791.
        ReadOnlySpan<byte> header = stackalloc byte[]
        {
            0x00, 0x00, 0x00, 0x12,
            0x00, 0x00, 0x00, 0x07,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x02,
        };
        Assert.Equal(0x6791, Crc16Ibm3740.Compute(header));
    }

    [Fact]
    public void Builder_RoundTripsBackToCapturedInitFrame()
    {
        var frame = MessageBuilder.Build(command: 0x07, param1: 0x00, param2: 0x02, payload: []);
        byte[] expected =
        {
            0x00, 0x00, 0x00, 0x12,
            0x00, 0x00, 0x00, 0x07,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x02,
            0x67, 0x91,
        };
        Assert.Equal(expected, frame);
    }

    [Fact]
    public void Matches_LongerCapturedFrame_PianoCategoriesResponse()
    {
        // Message 14 from parsed_protocol.txt: length=0x73, cmd=0x0c, param1=0x0a, param2=0x03,
        // 97-byte payload listing piano category strings. Captured CRC = 0x80ae.
        byte[] frameWithoutCrc =
        {
            0x00, 0x00, 0x00, 0x73,
            0x00, 0x00, 0x00, 0x0c,
            0x00, 0x00, 0x00, 0x0a,
            0x00, 0x00, 0x00, 0x03,
            // payload
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x06, 0x00, 0x00, 0x00, 0x05, 0x47, 0x72, 0x61,
            0x6e, 0x64, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x07, 0x55, 0x70, 0x72, 0x69, 0x67, 0x68,
            0x74, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x08, 0x45, 0x6c, 0x65, 0x63, 0x74, 0x72, 0x69,
            0x63, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x08, 0x43, 0x6c, 0x61, 0x76, 0x2f, 0x48, 0x70,
            0x73, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x07, 0x44, 0x69, 0x67, 0x69, 0x74, 0x61, 0x6c,
            0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x05, 0x4c, 0x61, 0x79, 0x65, 0x72, 0x00, 0x00, 0x00,
            0x14,
        };
        Assert.Equal(0x80ae, Crc16Ibm3740.Compute(frameWithoutCrc));
    }

    [Fact]
    public void Parser_ExtractsCategoryStrings()
    {
        // Same response frame as above. Parser should pull Grand, Upright, Electric, Clav/Hps, Digital, Layer.
        var frame = new byte[]
        {
            0x00, 0x00, 0x00, 0x73,
            0x00, 0x00, 0x00, 0x0c,
            0x00, 0x00, 0x00, 0x0a,
            0x00, 0x00, 0x00, 0x03,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x06, 0x00, 0x00, 0x00, 0x05, 0x47, 0x72, 0x61,
            0x6e, 0x64, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x07, 0x55, 0x70, 0x72, 0x69, 0x67, 0x68,
            0x74, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x08, 0x45, 0x6c, 0x65, 0x63, 0x74, 0x72, 0x69,
            0x63, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x08, 0x43, 0x6c, 0x61, 0x76, 0x2f, 0x48, 0x70,
            0x73, 0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x07, 0x44, 0x69, 0x67, 0x69, 0x74, 0x61, 0x6c,
            0x00, 0x00, 0x00, 0x14, 0x00, 0x00, 0x00, 0x05, 0x4c, 0x61, 0x79, 0x65, 0x72, 0x00, 0x00, 0x00,
            0x14,
            0x80, 0xae,
        };
        Assert.True(MessageParser.TryParse(frame, out var msg));
        Assert.True(MessageParser.VerifyCrc(frame, msg));

        var strings = MessageParser.ScanLengthPrefixedStrings(msg.Payload).ToArray();
        Assert.Equal(new[] { "Grand", "Upright", "Electric", "Clav/Hps", "Digital", "Layer" }, strings);
    }
}
